// Translation helper functions
class TranslationManager {
    constructor() {
        this.currentLang = localStorage.getItem('preferred-language') || 'en';
        this.init();
    }
    
    init() {
        // Add language switcher to the page if not exists
        if (!document.querySelector('.language-switcher')) {
            this.createLanguageSwitcher();
        }
        this.applyLanguage(this.currentLang);
    }
    
    createLanguageSwitcher() {
        const switcher = document.createElement('div');
        switcher.className = 'language-switcher';
        switcher.innerHTML = `
            <button class="lang-btn ${this.currentLang === 'en' ? 'active' : ''}" data-lang="en">EN</button>
            <button class="lang-btn ${this.currentLang === 'ru' ? 'active' : ''}" data-lang="ru">RU</button>
            <button class="lang-btn ${this.currentLang === 'pt' ? 'active' : ''}" data-lang="pt">PT</button>
            <button class="lang-btn ${this.currentLang === 'fr' ? 'active' : ''}" data-lang="fr">FR</button>
            <button class="lang-btn ${this.currentLang === 'zh' ? 'active' : ''}" data-lang="zh">中文</button>
            <button class="lang-btn ${this.currentLang === 'ar' ? 'active' : ''}" data-lang="ar">العربية</button>
        `;
        
        // Insert after header
        const header = document.querySelector('header');
        if (header) {
            header.insertAdjacentElement('afterend', switcher);
        } else {
            document.body.insertAdjacentElement('afterbegin', switcher);
        }
        
        // Add event listeners
        switcher.querySelectorAll('.lang-btn').forEach(btn => {
            btn.addEventListener('click', (e) => {
                const lang = e.target.dataset.lang;
                this.switchLanguage(lang);
            });
        });
    }
    
    switchLanguage(lang) {
        this.currentLang = lang;
        localStorage.setItem('preferred-language', lang);
        this.applyLanguage(lang);
        
        // Update active button
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.remove('active');
            if (btn.dataset.lang === lang) {
                btn.classList.add('active');
            }
        });
    }
    
    applyLanguage(lang) {
        // Update HTML direction for Arabic
        if (lang === 'ar') {
            document.documentElement.dir = 'rtl';
            document.documentElement.lang = 'ar';
            // Add Arabic font class
            document.documentElement.classList.add('rtl-lang');
        } else {
            document.documentElement.dir = 'ltr';
            document.documentElement.lang = lang;
            document.documentElement.classList.remove('rtl-lang');
        }
        
        // Apply translations to all elements with data-translate attribute
        document.querySelectorAll('[data-translate]').forEach(element => {
            const key = element.getAttribute('data-translate');
            if (translations[lang] && translations[lang][key]) {
                element.textContent = translations[lang][key];
            }
        });
        
        // Apply translations to placeholders
        document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
            const key = element.getAttribute('data-translate-placeholder');
            if (translations[lang] && translations[lang][key]) {
                element.placeholder = translations[lang][key];
            }
        });
        
        // Apply translations to hrefs
        document.querySelectorAll('[data-translate-href]').forEach(element => {
            const key = element.getAttribute('data-translate-href');
            if (translations[lang] && translations[lang][key]) {
                element.href = translations[lang][key];
            }
        });
        
        // Update Typed.js if exists
        this.updateTypedText(lang);
        
        // Update modal buttons
        this.updateModalButtons(lang);
    }
    
    updateTypedText(lang) {
        if (window.typedInstance && translations[lang]) {
            const typedStrings = [
                translations[lang]['typed_1'],
                translations[lang]['typed_2'],
                translations[lang]['typed_3'],
                translations[lang]['typed_4']
            ].filter(Boolean);
            
            // Destroy existing instance
            window.typedInstance.destroy();
            
            // Create new instance
            const typedElement = document.querySelector('.typedText');
            if (typedElement) {
                window.typedInstance = new Typed('.typedText', {
                    strings: typedStrings,
                    typeSpeed: 70,
                    backSpeed: 60,
                    loop: true,
                    startDelay: 500,
                    backDelay: 1500
                });
            }
        }
    }
    
    updateModalButtons(lang) {
        // Update modal buttons if modal is open
        const modalCloseBtn = document.querySelector('.modal .btn-primary');
        const modalGitBtn = document.querySelector('#modalGit');
        
        if (modalCloseBtn && translations[lang] && translations[lang]['modal_close']) {
            modalCloseBtn.textContent = translations[lang]['modal_close'];
        }
        
        if (modalGitBtn && translations[lang] && translations[lang]['modal_view_github']) {
            modalGitBtn.textContent = translations[lang]['modal_view_github'];
        }
    }
    
    t(key) {
        return translations[this.currentLang]?.[key] || translations['en'][key] || key;
    }
}

